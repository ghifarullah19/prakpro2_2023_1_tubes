    public void setPanelData(Penjemputan pelanggan) {
        namaKurir.setText(pelanggan.getNama());
        alamatPenjemputan.setText(pelanggan.getAlamat());
        
        iconBox.setText("");
        iconBox.setIcon(imageIcon("C:\\Users\\malwi\\OneDrive\\Documents\\NetBeansProjects\\techin\\src\\main\\java\\id\\ac\\unpas\\techIn\\img\\box.png",
                65,
                65));
        
        if (pelanggan.getStatus()) {
            buttonDetail.setText("Tidak Menjemput");
            buttonDetail.setBackground(Color.green);
        } else {
            buttonDetail.setText("Menjemput");
            buttonDetail.setBackground(Color.red);
    
        }
    }
    
    public ImageIcon imageIcon(String imageURL, int height, int width) {
        ImageIcon icon = new ImageIcon(imageURL);
        Image image = icon.getImage();
        Image newImage = image.getScaledInstance(width,
                        height,
                        Image.SCALE_SMOOTH);
        icon = new ImageIcon(newImage);
        return icon;
    }

